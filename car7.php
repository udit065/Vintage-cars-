	<h1 align="center">Vehicle Description</h2>
	<p  align="left" class="bg-light">Ferrari made just 39 of these elegant race cars between 1962 and 1964, so they are extremely rare. "The 250 GTO is probably the holy grail in terms of value and recognition, but the reality is only a handful of people in the world will ever have the chance to legitimately drive one," Hagerty says. An early model fetched $38 million at Bonhams' Quail Lodge auction at the Pebble Beach Concours d'Elegance two years ago. If that's too pricey, know that any 1960s Prancing Horse with a V12 engine is worthy of making the list.<br><br>
     Then there’s the company’s glorious racing heritage. For instance, while the 1984 Testarossa was a spectacular road car, it never made it onto the race track. The same goes for many of the brand’s sports cars. The 250 GTO, on the other hand, had it all. Gorgeous styling, power, speed, championship-winning engineering, and exclusivity. I dare you to name any other Ferrari to boast such an impressive package.<br><br>
     The 250 GTO’s 3.0-liter V-12 engine was borrowed from the 250 Testa Rossa. Although not exactly new, the mill was very reliable, being used with huge success in endurance racing by the latter. Developed by Gioacchino Colombo in the late 1950s, the engine was an all-alloy design utilizing a dry sump and six Weber carburetors. The twelve-banger cranked out 302 horsepower at 7,500 rpm and 246 pound-feet of torque at 5,500 rpm and enabled the 250 GTO to reach 60 mph from a standing start in only 5.4 seconds. The GT was quick enough to run the quarter mile in 13.1 seconds and reached a top speed of 174 mph. All outstanding figures for the early 1960s.<br><br>

While the engine was already a few years old, the transmission was a brand-new, five-speed manual with Porsche-type synchromesh. Later models received minor engine modifications that brought a slight increase in power and torque.



