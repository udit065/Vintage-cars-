	<h1 align="center">Vehicle Description</h2>
	<p  align="left" class="bg-light">THE LEGENDARY GULLWING

Mercedes-Benz's 300 SL claimed 2nd in the Mille Miglia, 1-2-3 in the Sports Car Race in Berne, Switzerland, 1st and 2nd at Le Mans, 1-2-3-4 at the Nï¿½rburgring, and 1st and 2nd at La Carrera Panamericana. Yet more was to be desired. From his Frank Lloyd Wright-designed Park Avenue showroom, the company's US distributor, Max Hoffman, said that there was a market in America for a fast, sensual Mercedes-Benz coupe, and a production version of the racing W194 300 SL, complete with the fascinating and now legendary "gullwing" doors, necessitated by the unusual "birdcage" frame design, would be it.<br><br>

The "SL" moniker (translated as "Sport Light") reflected the pioneering use of a welded, tubular-steel, ultra-light frame construction that weighed only 182 lb. The car also featured fully independent suspension in addition to its fuel-injected, 2,996 cc, OHC straight-six with dry-sump lubrication, and the engine was inclined to the side in order to reduce the height of the front end. The power, rated at 240 bhp at 6,100 rpm (SAE) and 215 bhp at 5,800 rpm (DIN), with the factory-optional or dealer-installed "sport" camshaft, was delivered through a four-speed manual transmission. A 161 mph top speed and 0-60 mph acceleration of approximately eight seconds, depending on the rear-end ratio selected from five options, made the 300 SL the fastest production automobile of its time.<br><br>

Appropriate for an automobile that Max Hoffman had almost single-handedly willed into being, the production 300 SL made its debut in the United States, not in Germany, which was a Mercedes-Benz first. More than 1,000 of the 1,400 cars produced between 1954 and early 1957 were delivered through Hoffman, to whose showrooms the rich and famous flocked. The 300 SL was as much a status symbol in its time as it is today, as it was favored by everyone from Hollywood stars to racing legends to genuine royalty.<br><br>

The 300 SL was also raced and piloted by the top drivers of the day, such as John Fitch, Olivier Gendebien, Paul O'Shea, Prince Metternich, and, of course, Sir Stirling Moss, who holds the "forever" course record for his famous Mille Miglia finish in 1955. It all added to the romance of a car that seemed destined to become a legend the moment production began. It had all the right ingredients: incredible exclusivity, incredible speed, and an incredible price.<br><br>

"A thoroughbred in every sense of the word," advertising boasted, "and a car which will be recognized by all enthusiasts as the 'last word' in sporting automobiles; a car which puts indescribable pleasure into driving!"<br><br>

THE MAGLIOLI GULLWING<br><br>

Chassis 5500533, offered here, was originally delivered through Milanese dealer Saporiti to one H. Maglioli, or Umberto Maglioli, the Italian racing driver who participated in ten Formula One World Championship Grands Prix between 1953 and 1957, and also won the Carrera Panamericana in 1954.<br><br>

The Mercedes-Benz data card, a copy of which is in the file, names Mr. Maglioli and notes the original engine, gearbox, and body numbers as matching the present units in the car, which still have their correct original stampings, as does the frame. Even the left and right front axles appear to be original, though the differential is a slightly later period-correct unit (possibly replaced for a different rear-end ratio). The car was originally delivered in Silver Gray Metallic (DB 180) and originally had the standard interior, fawn seats with red tartan inserts. Significantly, it was also specified with a Becker Mexico radio, fitted luggage, and one of the most desirable options of all, factory Kronprinz wheels with Rudge center-lock knock-offs.<br><br>

Later owners of the car included another well-known racing driver and Mercedes-Benz specialist, Lothar Motschenbacher, as well as a noted Mercedes-Benz collector in Southern California. As of 2010 it was recorded in the Gullwing Group's registry as having 72,000 miles and having been restored with the present red leather interior and original-style luggage, as well as the original Rudge wheels-just as it remains today. That same year it was sold into the current collection, where it has been maintained since.<br><br>

The restoration appears to have been largely cosmetic, as the chassis and engine compartment both retain extensive original finishes and attractive patina from road use. The bodywork is quite good, with solid fender "eyebrows," while the interior is beautifully finished and still quite fresh in its appearance, including crisp, clear gauges and tight upholstery. A wood-rimmed Nardi steering wheel is the only readily apparent deviation from "stock."<br><br>

Accompanying the car is the original spare, knock-off hammer, tool kit, the padded bag for the side windows, and a reproduction manual-everything needed to continue to enjoy a wonderful Gullwing on the road. It would be an ideal entrant for any number of vintage road tours and rallies for which the 300 SL is happily eligible, and promises to be extremely exciting to drive. Umberto Maglioli would undoubtedly approve of his former mount being enthusiastically run and enjoyed exactly as he would have in the mid-1950s, at the height of his illustrious career.To view this car and others currently consigned to this auction, please visit the RM website at rmsothebys.com/en/auctions/el20.


