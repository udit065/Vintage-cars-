  <h1 align="center">Vehicle Description</h2>
  <p  align="left" class="bg-light">The McLaren F1 is a sports car designed and manufactured by British automobile manufacturer McLaren Cars, and powered by the BMW S70/2 V12 engine. Originally a concept conceived by Gordon Murray, he convinced Ron Dennis to back the project and engaged Peter Stevens to design the exterior and interior of the car. On 31 March 1998, the XP5 prototype with a modified rev limiter set the Guinness World Record for the world's fastest production car, reaching 240.1 mph (386.4 km/h), surpassing the modified Jaguar XJ220's 217.1 mph (349 km/h) record from 1992.<br>
    <br>

The car features numerous proprietary designs and technologies; it is lighter and has a more streamlined structure than many modern sports cars, despite having one seat more than most similar sports cars, with the driver's seat located in the centre (and slightly forward) of two passengers' seating positions, providing driver visibility superior to that of a conventional seating layout. It was conceived as an exercise in creating what its designers hoped would be considered the ultimate road car. Despite not having been designed as a track machine, a modified race car edition of the vehicle won several races, including the 1995 24 Hours of Le Mans, where it faced purpose-built prototype race cars. Production began in 1992 and ended in 1998. In all, 106 cars were manufactured, with some variations in the design.<br><br>

In 1994, the British car magazine Autocar stated in a road test regarding the F1, "The McLaren F1 is the finest driving machine yet built for the public road." They further stated, "The F1 will be remembered as one of the great events in the history of the car, and it may possibly be the fastest production road car the world will ever see." In 2005, Channel4 placed the car at number one on their list of the 100 greatest cars, calling it "the greatest automotive achievement of all time". In popular culture, the McLaren F1 has earned its spot as 'The greatest automobile ever created' and 'The Most Excellent Sports Car Of All Time' amongst a wide variety of car enthusiasts and lovers. Notable past and present McLaren F1 owners include Elon Musk,[6] Rowan Atkinson, Jay Leno, George Harrison, and the Sultan of Brunei. In the April 2017 issue of Top Gear Magazine, the McLaren F1 was listed as one of the fastest naturally aspirated cars currently available in the world, and in the same league as the more modern vehicles such as the Ferrari Enzo and Aston Martin One-77 despite being produced and engineered 10 years prior the Ferrari Enzo and 17 years prior the Aston Martin One-77.
