	<h1 align="center">Vehicle Description</h2>
	<p  align="left" class="bg-light">Based on the first full-size car Rolls-Royce made after the war, the original Silver Dawn drophead launched in 1949 and retired in 1954. The name was intended to mark the dawn of a new era for the world and Rolls-Royce's place in it. Slightly smaller than pre-war cars, the Dawn helped the British bespoke carmaker reintroduce motoring craftsmanship while bringing the company into the modern age. They're extremely rare: only three of the original 28 dropheads remain in the U.S., and those are owned by private collectors. Happily, the carmaker is introducing a successor model after a 60-year hiatus.<br><br>
    The Rolls-Royce Silver Dawn is a full-size luxury car that was produced by Rolls-Royce at their Crewe works between 1949 and 1955. It was the first Rolls-Royce car to be offered with a factory built body which it shared, along with its chassis, with the Bentley Mark VI until 1952 and then the Bentley R Type until production finished in 1955. The car was first introduced as an export only model. The left hand drive manual transmission models had a column gear change, while right hand drives had a floor change by the door. Only with the R Type based model was it officially available on the home market, from October 1953.<br><br>
    In 1951 the Silver Dawn was upgraded to the 4 1/2 L engine and the full flow oil filter (Chassis Number LSFC2). In 1953 the high compression cylinder head was fitted from Chassis number SMF66 and from the same chassis number on the Bentley Mk VI type camshaft was fitted to left hand drive vehicles<br><br>

    The in-line six cylinder engine had overhead inlet and side exhaust valves and had a capacity of 4,257 cc (259.8 cu in) until 1951 when it was enlarged to 4,566 cc (278.6 cu in). The carburettor up to Chassis number SFC100 was a single double downdraught Stromberg type AAV 26 until 1952 when it was replaced by a Zenith DBVC42.<br><br>

A four speed manual gearbox was fitted to all cars at first, with a 4-speed automatic becoming an option in late 1952 on the 'E' Series chassis, and on the corresponding Bentley 'R' Type chassis. There are conflicting reports if the automatic gearbox became standard in the Silver Dawn, but both manual and automatic options were available until the end of the production run.<br><br>

The suspension was independent at the front using coil springs while at the rear the live axle used half elliptic leaf springs. The car had a separate chassis made with traditional riveted construction until 1953 after which it was welded. Servo assisted 12.25 in (311 mm) drum brakes were used, hydraulically operated at the front but retaining mechanical operation at the rear. Although many cars were fitted with factory built bodies, others were supplied to external coachbuilders.


</p>

